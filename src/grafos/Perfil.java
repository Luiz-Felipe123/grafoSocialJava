package grafos;

import java.util.HashMap;
import java.util.Map;

public class Perfil {
    private int id;
    private String nome;
    private String interesses;

    public Perfil(String nome, String interesses) {
        this.id = id;
        this.nome = nome;
        this.interesses = interesses;
    }

    public int getId() {
        return id;
    }

    public String getNome() {
        return nome;
    }

    public String getInteresses() {
        return interesses;
    }
}

